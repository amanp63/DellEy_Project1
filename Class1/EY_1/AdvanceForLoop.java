package EY_1;

public class AdvanceForLoop {
	public static void main(String[] args) {
		int[] intArr = { 1, 2, 3, 45, 6, 7, 8 };
		for (int data : intArr) {
			System.out.println(data + " ");
		}
		System.out.println();
		String[] strArr = { "z", "A", "V", "B" };

		for (String str : strArr) {
			System.out.println(str + " ");
		}
	}

}
